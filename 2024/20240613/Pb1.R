ellipse(center = c(Md), shape = Sd/n, radius = sqrt(cfr.fisher), lwd = 2, col = 'grey', center.cex = 1.25)
abline(h = 0)
abline(v = 0)


# d) ----------------------------------------------------------------------

k <- q-1   # number of increments (i.e., dim(C)[1])
