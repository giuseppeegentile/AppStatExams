M <- sapply(data, mean)
S <- cov(data)

set.seed(20240613)
X <- rmvnorm(n = 10000, mean = M, sigma = S)
head(X)

for(i in 1:dim(data)[1])
{
  print(paste("Data element #", i, ": ", sep = ''))
  print(data[i, ])
  print("Various randomly generated elements with a similar first variable:")
  print(X[X[, 1] >= (data[i, 1] - 0.1) & X[, 1] <= (data[i, 1] + 0.1), ])
}
# It seems like the rows of the data matrix have been independently generated by the same tri-variate Gaussian distribution


# b) ----------------------------------------------------------------------

n <- dim(data)[1]
q <- dim(data)[2]

C <- matrix(c(-1, 1, 0,
              -1, 0, 1), q-1, q, byrow = T)
C

alpha <- .05
delta.0 <- c(0, 0)

Md <- C %*% M 
Sd <- C %*% S %*% t(C) 
Sdinv <- solve(Sd)

T2 <- n * t(Md - delta.0) %*% Sdinv %*% (Md - delta.0)
cfr.fisher <- ((q - 1) * (n - 1) / (n - (q - 1))) * qf(1 - alpha, (q - 1), n - (q - 1)) 

T2 < cfr.fisher
# Yes, there's a taster which over/under-prices the bottles compared to the two others

P <- 1 - pf(T2 * (n - (q - 1)) / ((q - 1) * (n - 1)), (q - 1), n - (q - 1))
P
# 0.006252078


